require 'json'
require 'logger'
require 'parslet'
require 'wrapsher'
require 'pry'

module Wrapsher
  class Generator
    def initialize(logger: nil, level: nil, type: :program)
      @logger = logger || Logger.new($stderr)
      @logger.level = level || :DEBUG
      @type = type
    end

    def preamble
      if @type == :program
        File.read(File.join(__dir__, 'preamble.sh'))
      else
        ''
      end
    end

    def find_test_functions(nodes)
      nodes.select do |node|
        node.is_a?(Wrapsher::Node::FunStatement) && node.signature.name.start_with?('test_')
      end
    end

    def wsh_preamble
      if @type == :program
        [Wrapsher::Parser.new.parsetext(File.read(File.join(__dir__, 'preamble.wsh')))].flatten
      else
        []
      end
    end

    def postamble
      if @type == :program
        File.read(File.join(__dir__, 'postamble.sh'))
      else
        ''
      end
    end

    def generate(program, tables: Wrapsher::ProgramTables.new)
      nodes = (wsh_preamble + [program].flatten).map do |obj|
        Wrapsher::Node.from_obj(obj, tables: tables)
      end

      errors = nodes.map(&:errors).flatten
      if errors.any?
        @logger.error "Compilation errors:"
        errors.each do |error|
          @logger.error "  - #{error}"
        end
        raise Wrapsher::CompilationError, "Compilation failed with #{errors.size} errors"
      end

      if @type == :program
        nodes = tables.to_nodes + nodes
      end

      [
        shebang,
        @type == :program ? showtables(tables) : '',
        preamble,
        nodes.map(&:to_s).join("\n"),
        postamble
      ].join("\n")
    end

    def shebang
      if @type == :program
        <<~EOF
        #!/bin/sh
        # Generated by Wrapsher
        EOF
      else
        '# included'
      end
    end

    def showtables(tables)
      <<~EOF
      : <<EOT
      #{tables.to_s}
      EOT
      EOF
    end
  end
end
