require 'json'
require 'logger'
require 'parslet'
require 'wrapsher'
require 'pry'

module Wrapsher
  class Generator
    def initialize(logger: nil, level: nil)
      @logger = logger || Logger.new($stderr)
      @logger.level = level || :DEBUG
    end

    def preamble
      File.read(File.join(__dir__, 'preamble.sh'))
    end

    def postamble
      File.read(File.join(__dir__, 'postamble.sh'))
    end

    def generate(program, filename: '-')
      case program
      when Array
        nodes = program.map { |obj| Wrapsher::Node.from_obj(obj, filename: filename) }
      when Hash
        nodes = [Wrapsher::Node.from_obj(program, filename: filename)]
      end
      <<~EOF
      #!/bin/sh
      # Generated by Wrapsher
      # #{program.to_s}
      : <<EOT
      #{nodes.inspect}
      EOT
      #{preamble}
      #{nodes.map(&:to_s).join("\n")}
      #{postamble}
      EOF
    end
  end
end
